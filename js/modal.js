        $(document).ready(function()
            {
                 $("#ModalPopUp").hide();
                //### Open Modal PopUp
                $("#myBtn").click(function()
                 {
                     $("#ModalPopUp").show();
                 });
                 //### Close Modal PopUp when Close button is clicked
                 $("#btnClose").click(function()
                 {
                     $("#ModalPopUp").hide();
                 });
                  $("#btnPost").click(function()
                 {
                     $("#ModalPopUp").hide();
                 });
			});

        